public class AddTwoIntegers {

    public static void main(String[] args) {
        
        int first = 10;
        int second = 20;

        System.out.println("The two numbers entered are: " + first + " " + second);
        int sum = first + second;

        for(int i = 1; i <= 5; i++) {
            System.out.println("The sum is: " + sum);
        }
    }
}
